package vehicles;

public class Avio {
        String model;
        long autonomia;

        public void setModel (String model){
                this.model=model;
        }

        public String getModel (){
                return model;
        }

        public void setAutonomia (long autonomia){
                this.autonomia=autonomia;
        }

        public long getAutonomia (){
                return autonomia;
        }

}
