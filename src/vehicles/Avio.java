package vehicles;

public class Avio {
        String model;
        float consum;

        public void setModel (String model){
                this.model=model;
        }

        public String getModel (){
                return model;
        }

        public void setConsum (float consum){
                this.consum=consum;
        }

        public float getConsum (){
                return consum;
        }

}
